# Визначення списку чисел
numbers = [10, 20, 30, 40, 50]

# Ініціалізація змінної для збереження суми
sum = 0

# Використання циклу for для обчислення суми чисел у списку
for number in numbers:
    sum += number  # Додаємо кожне число до суми

# Виведення результату
print(f"Сума чисел у списку: {sum}")